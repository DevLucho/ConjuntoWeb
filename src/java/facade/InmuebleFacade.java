package facade;

import entidades.Inmueble;
import java.util.List;
import javax.ejb.Stateless;
import javax.persistence.EntityManager;
import javax.persistence.PersistenceContext;
import javax.persistence.Query;

/**
 *
 * @author Huertas
 */
@Stateless
public class InmuebleFacade extends AbstractFacade<Inmueble> {

    @PersistenceContext(unitName = "ProyectoConjuntoWebPU")
    private EntityManager em;

    @Override
    protected EntityManager getEntityManager() {
        return em;
    }

    public InmuebleFacade() {
        super(Inmueble.class);
    }

    public List<Inmueble> findInmueble() {
        Query query = em.createQuery("SELECT u FROM Inmueble u ORDER BY u.idTorre.idTorre ASC");
        return query.getResultList();
    }

}
